 <b>Почему визуальная поддержка так важна?</b>
				</p>
				<p>
					 Основные признаки расстройства аутичного спектра - трудности с социальным взаимодействием, проблемы с речью, ограниченные интересы и повторяющееся поведение.&nbsp;
				</p>
				<p>
					 Детям с РАС трудно понять социальные знаки во время повседневного взаимодействия с другими людьми. Они могут не понимать, что от них ожидают в социальных ситуациях, как начать разговор, ответить на попытки социального взаимодействия других людей или изменить свое поведение. Визуальная поддержка может помочь в обучении социальным правилам, дети с аутизмом могут сами использовать ее в социальных ситуациях.
