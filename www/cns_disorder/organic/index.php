				</p>
				<p>
					 Некоторые дети с РАС испытывают очень сильную тревожность и могут плохо себя вести, если привычный распорядок дня как-то меняется, или они оказываются в незнакомой ситуации. Визуальная поддержка помогает им понять, чего ожидать, что произойдет потом, и это уменьшает их тревожность.
