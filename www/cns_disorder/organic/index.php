					 Если возникает проблемное поведение, продолжайте физически подсказывать ребенку выполнение задания «сначала». Сосредоточьте внимание на выполнении задания, а не на проблемном поведении. После этого все равно важно предоставить приятное занятие или желанный предмет, так как целью доски было выполнение задания «сначала», а не работа с проблемным поведением.
				</p>
				<p>
					 Если вы считаете вероятным возникновение проблемного поведения, то поначалу включайте в доску «Сейчас-потом» только те задания, которые ребенок выполняет с легкостью и охотно.
