 <b>Поведение маленького аутиста</b>
				</p>
				<p>
					 Аутизм ярко проявляет себя в нарушении коммуникативной функции, контакта ребенка с окружающими людьми. Малыш живет в мире зрительных образов, звуков, тактильных ощущений. Он не стремится поделиться своим миром, впечатлениями с мамой или папой. Родители выполняют для него исключительно инструментальную функцию, являясь источниками пищи, тепла, комфорта. Для детей характерны навязчивые действия: кручение предметов в руках, наблюдение за льющейся из крана водой, выстраивание в ряд игрушек, однообразные игры. Они могут долго кружиться на одном месте или кругами ходить по комнате на цыпочках.
