				<p>
 <b>Визуальная поддержка </b>- это использование картинок или других наглядных предметов для сообщения информации ребенку, которому трудно понимать и использовать речь. Можно применять фотографии, рисунки, трехмерные предметы, написанные слова или списки. Исследования показали очень высокую эффективность визуальной поддержки для аутичных детей.
				</p>
				<p>
					 Визуальная поддержка применяется по двум основным причинам: она облегчает коммуникацию родителей со своим ребенком и упрощает коммуникацию малыша с другими людьми.
