				<p>
					 Доска «Сейчас-потом» - это визуальная последовательность из двух изображений. Доска сообщает о некоем приятном событии, которое произойдет после завершения не такой приятной задачи.
				</p>
				<p>
 <b>Когда этим пользоваться?</b>
				</p>
				<p>
					 Доска полезна, если вы учите ребенка с РАС следовать указаниям и новым навыкам. Она мотивирует ребенка сделать что-то, что ему не нравится, потому что потом последует что-то приятное. Этот инструмент обучает ребенка языку для понимания инструкций из нескольких шагов. Если ребенок научился понимать доску «Сейчас-потом» - это поможет ему усваивать и применять более сложные системы визуальной поддержки.
				</p>
				<p>
 <b>Как этим пользоваться?</b>
				</p>
				<p>
				</p>
				<ul>
					<li>Решите, какое задание ребенок выполняет сначала (это будет изображение под надписью «сначала»), и какое приятное занятие или награда ждет его сразу же после выполнения (картинка под надписью «потом»). Что следует «потом» должно быть достаточно мотивирующим, только тогда есть высокая вероятность, что ребенок будет следовать вашей инструкции.</li>
					<li>Расположите на доске изображения (например, фотографии, рисунки, написанные слова), которые символизируют первое и второе занятие.</li>
					<li>Покажите доску ребенку вместе с короткой устной инструкцией. Старайтесь употреблять как можно меньше слов, начните со слова «сначала». Например: «Сначала надень ботинки, потом качели».</li>
					<li>По необходимости напоминайте ребенку о доске во время выполнения задания. Например: «Еще один ботинок, потом качели».</li>
					<li>Когда задание «сначала» выполнено, снова привлеките внимание ребенка к доске. Например: «Ты надел ботинки, теперь качели!». Немедленно предоставьте ребенку обещанный предмет или доступ к приятному занятию.</li>
					<li>Для того, чтобы дети с РАС начали ценить доску «Сейчас-потом», им необходимо получать приятное занятие или желанный предмет сразу же после выполнения задания «сначала». В противном случае ребенок больше не будет доверять тому, что изображено на доске.</li>
				</ul>
