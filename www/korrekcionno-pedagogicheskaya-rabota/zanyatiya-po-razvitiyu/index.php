		<h2>Коррекционная работа при ДЦП</h2>
		<p>
			 В настоящее время увеличивается распространенность детского церебрального паралича среди других видов нарушений. Согласно психофизиологическим данным, у здоровых детей в норме:
		</p>
		<ul>
			<li>кинестетическое, предметно-зрительное восприятие, тактильные функции развиваются к 4–5 годам;</li>
