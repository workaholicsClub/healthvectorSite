		<p>Выраженные двигательные расстройства и нарушения речи при детском церебральном параличе затрудняют общение детей с окружающими, способствуют формированию негативных черт характера, появлению поведенческих нарушений, формированию острого чувства неполноценности. Но эти дети являются полноценными членами общества и необходимо обеспечить все условия для их полноценного личностного развития.</p>

