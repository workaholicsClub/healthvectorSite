		</p>
		<h2>Когда начинать занятия с ребенком?</h2>
		<p>
			 Чем раньше – тем лучше, с первых дней жизни. Вначале занятия будут самыми простыми. Во время воздушных ванн и купания позвольте малышу свободно двигать ручками и ножками. Постепенно к играм добавятся яркие игрушки. К 2-3 месяцам показывайте ребенку игрушку, привлекая к ней внимание. При этом малыш следит за ней, поворачивает голову. Это упражнение проводится, когда ребенок лежит на животе. Играйте и развлекайте малыша разными игрушками: упражнения будут стимулировать слух (позвенеть с разных сторон колокольчиком), зрение (наблюдение за яркой игрушкой). Если дать малышу погремушку в руки - будет задействована моторика пальцев и это поможет развитию хватательного рефлекса. Постепенно, с помощью игр, ребенок познает окружающий мир и развивается.
		</p>
