			<li>структурно-топологические и координатные функции — к 6–7 летнему возрасту,</li>
			<li>мозолистое тело, координирующее одновременную работу обоих полушарий, интенсивно развивается до 7 лет.</li>
		</ul>
		<p>
			 В отличие от сверстников, у детей с церебральным параличом наблюдаются нарушения в развитии двигательных, психических, речевых функций. ДЦП характеризуется повреждением всех структур двигательного анализатора мозга.
		</p>
		<p>
			 При грамотной реабилитации ребенка с ДЦП можно научить сидеть, ходить, одним словом - обеспечить практически полноценную жизнь. Необходимо как можно раньше начинать работу с ребенком, которому поставили такой диагноз.
