		<h2>Физическая нагрузка при ДЦП</h2>

		<p>Физическая нагрузка должна быть направлена на релаксацию и понижение тонуса мышц. Спорт и ДЦП волне совместимы. Малышу подойдет иппотерапия, гидрокинезотерапия, ЛФК, занятия на фитболе. Подробнее о методах физической нагрузки, вы можете прочитать на нашем портале в разделе <a href="/treatment/">реабилитация</a>.</p>
		<p>Большинство занятий легко организовать дома, используя обычные вещи. Например, игры с сыпучими и жидкими составами (крупы, гель) помогут развить моторику. Отлично помогает в разработке движений запястий и пальцев рисование мелками, карандашами, лепка из теста и глины. Окружающий быт поможет развить осязание, (малыш узнает на ощупь ткани и предметы разной фактуры), увеличить словарный запас (разговаривая с малышом, называя окружающие предметы которыми он пользуется в повседневной жизни). </p>
		<p>Важно иметь в виду, что организация окружающей среды должна максимальным образом стимулировать развитие ребенка, сглаживать негативное влияние заболевания на его психическое состояние.</p>
		
