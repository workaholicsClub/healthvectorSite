		<h2>Коррекционная работа при ДЦП</h2>
		<p>В настоящее время увеличивается распространенность детского церебрального паралича среди других видов нарушений. Согласно психофизиологическим данным, у здоровых детей в норме:</p>
		<ul>
			<li>кинестетическое, предметно-зрительное восприятие, тактильные функции развиваются к 4–5 годам;</li>
			<li>соматогностические функции — к 6 годам;</li>
			<li>структурно-топологические и координатные функции — к 6–7 летнему возрасту,</li>
			<li>мозолистое тело, координирующее одновременную работу обоих полушарий, интенсивно развивается до 7 лет.</li>
		</ul>
	
		<p>В отличие от сверстников, у детей с церебральным параличом наблюдаются нарушения в развитии двигательных, психических, речевых функций. ДЦП характеризуется повреждением всех структур двигательного анализатора мозга.</p>
		<p>При грамотной реабилитации ребенка с ДЦП можно научить сидеть, ходить, одним словом - обеспечить практически полноценную жизнь. Необходимо как можно раньше начинать работу с ребенком, которому поставили такой диагноз.</p>

		<div class="characteristics-content">
			<div class="b-text-select">
				Основными целями коррекционной работы при ДЦП являются:<br>
				оказание детям медицинской, психологической, педагогической, логопедической и социальной помощи, обеспечение максимально полной и ранней социальной адаптации, общего и профессионального обучения.
			</div>
		</div>
		<p><b>Чтобы коррекционная программа имела максимальное количество плюсов и давала хорошие результаты необходимо придерживаться следующих принципов: </b></p>
		<ul>
			<li>Проводить занятия ежедневно, иногда несколько раз в день. Регулярность крайне важна.</li>
			<li>Индивидуальный подход к ребенку. Важно учитывать все особенности заболевания, возраст ребенка, его психологическое состояние.</li>
			<li>Постепенное увеличение физических нагрузок, которые зависят от возможностей ребенка.</li>
			<li>Проведение коррекционных занятий с детьми с ДЦП в раннем детстве важно тем, что ребенок в этом возрасте способен лучше восстанавливать поврежденные участки и компенсировать нарушенные функции.</li>
			<li>Очень важно делать регулярный массаж тела ребенка, чтобы восстанавливать и увеличивать тонус мышц.</li>
		</ul>
		
		<p>В современном мире есть различные центры помощи для детей с ДЦП. Помимо профессиональной помощи специалистов по реабилитации, не менее важны упражнения, которые проводят родители с ребенком.</p>

		<h2>Когда начинать занятия с ребенком?</h2>
		<p>Чем раньше – тем лучше, с первых дней жизни.<br>
			Вначале занятия будут самыми простыми. Во время воздушных ванн и купания позвольте малышу свободно двигать ручками и ножками. Постепенно к играм добавятся яркие игрушки. К 2-3 месяцам показывайте ребенку игрушку, привлекая к ней внимание. При этом малыш следит за ней, поворачивает голову. Это упражнение проводится, когда ребенок лежит на животе.<br>
			Играйте и развлекайте малыша разными игрушками: упражнения будут стимулировать слух (позвенеть с разных сторон колокольчиком), зрение (наблюдение за яркой игрушкой). Если дать малышу погремушку в руки - будет задействована моторика пальцев и это поможет развитию хватательного рефлекса. Постепенно, с помощью игр, ребенок познает окружающий мир и развивается.
		</p>
		
		<div class="characteristics-content">
			<div class="b-text-select">
