				</p>
				<p>
					 При этом, предусмотренные этим Федеральным законом <b><u>меры социальной защиты инвалидов являются расходными обязательствами Российской Федерации</u>, за исключением мер социальной поддержки и социального обслуживания, относящихся к полномочиям государственной власти субъектов Российской Федерации в соответствии</b> с законодательством Российской Федерации.
				</p>
				<p>
					 Понятия «социальная защита инвалидов» и «социальная поддержка инвалидов» содержатся в статье 2 ФЗ № 181-ФЗ, согласно которой:
				</p>
				<ul>
					<li><b>социальная защита инвалидов</b> - система гарантированных государством экономических, правовых мер и мер социальной поддержки, обеспечивающих инвалидам условия для преодоления, замещения (компенсации) ограничений жизнедеятельности и направленных на создание им равных с другими гражданами возможностей участия в жизни общества (в ред. Федерального закона от 22.08.2004 N 122-ФЗ).</li>
					<li><b>социальная поддержка инвалидов</b> - система мер, обеспечивающая социальные гарантии инвалидам, устанавливаемая законами и иными нормативными правовыми актами, за исключением пенсионного обеспечения.</li>
				</ul>
				<p>
					 Из предложенных в законодательстве понятий следует, что социальная поддержка инвалидов является <b>частью социальной защиты</b>, наряду и экономическими и правовыми мерами, и одновременно <b>способом (комплексом мер) реализации социальной защиты</b>.
				</p>
				<p>
					 Вновь обращаясь к преамбуле закона, мы видим, что меры социальной защиты инвалидов являются расходными обязательствами Российской Федерации, за исключением мер социальной поддержки и социального обслуживания, относящихся к полномочиям государственной власти субъектов Российской Федерации.
				</p>
				<p>
					 Таким образом, четкого разграничения компетенций между Российской Федерацией и субъектами федерации нет. Уже в преамбуле закона ФЗ № 181-ФЗ появляется правовая неопределенность и возникает вопрос: являются ли меры социальной поддержки и социального обслуживания расходными обязательствами РФ, либо это расходные обязательства и ответственность субъектов федерации. Если же это сфера общей ответственности, то в какой части и какие бюджеты должны брать на себя бремя выполнения этих обязательств перед инвалидами.
				</p>
				<p>
					 Отсутствие правовой определенности в понятиях дополняется и иными нормами федерального законодательства, единообразие в терминологии, которых также отсутствует.
				</p>
				<p>
					 Так, например, статья 3 ФЗ № 442-ФЗ содержит основные понятия, используемые этом Федеральном законе, а именно:
				</p>
				<p>
					 1) <u>социальное обслуживание граждан</u> (далее - социальное обслуживание) - деятельность по предоставлению социальных услуг гражданам;
				</p>
				<p>
					 2) действие или действия в сфере социального обслуживания по оказанию постоянной, периодической, разовой помощи, в том числе срочной помощи, гражданину в целях улучшения условий его жизнедеятельности и (или) расширения его возможностей самостоятельно обеспечивать свои основные жизненные потребности;
				</p>
				<p>
					 3) получатель социальных услуг - гражданин, который признан нуждающимся в социальном обслуживании и которому предоставляются социальная услуга или социальные услуги;
				</p>
				<p>
					 4) поставщик социальных услуг - юридическое лицо, независимо от его организационно-правовой формы, и (или) индивидуальный предприниматель, осуществляющие социальное обслуживание;
				</p>
				<p>
					 5) стандарт социальной услуги - основные требования к объему, периодичности и качеству предоставления социальной услуги получателю социальной услуги, установленные по видам социальных услуг;
				</p>
				<p>
					 6) <u>профилактика обстоятельств, обусловливающих нуждаемость в социальном обслуживании</u> - система мер, направленных на выявление и устранение причин, послуживших основанием ухудшения условий жизнедеятельности граждан, снижения их возможностей самостоятельно обеспечивать свои основные жизненные потребности».
				</p>
				<p>
					 Из представленных в норме этой статьи понятий следует вывод о том, закон № 442-ФЗ имеет более широкий спектр применения, чем специальный ФЗ № 181-ФЗ, защищающий только права инвалидов.
				</p>
				<p>
					 В Законе № 442-ФЗ содержится более широкое понятие получателя <b><u>социальных услуг</u></b> как гражданина, признанного нуждающимся в социальном обслуживании и получении социальных услуг. К таким получателям относятся, как инвалиды, так и иные граждане, признанные получателями соц. услуг. К числу обстоятельств, при наличии которых граждане признаются нуждающимися в социальном обслуживании и получении социальных услуг (ст. 15 ФЗ № 442-ФЗ) относятся полная или частичная утрата способности либо возможности осуществлять самообслуживание, самостоятельно передвигаться, обеспечивать основные жизненные потребности в силу заболевания, травмы, возраста или <b><u>наличия инвалидности, наличие в семье инвалида, в т. ч. ребенка инвалида</u></b>.
				</p>
				<p>
	 <b>Таким образом, исходя из данного определения, следует, что инвалидность, в том числе инвалидность в несовершеннолетнем возрасте, является основанием для применения федерального закона № 442-ФЗ наряду со специальным федеральным законом о защите прав инвалидов № 181-ФЗ. Между тем, понятие «социальная защита» в законе № 442-ФЗ отсутствует.</b>
				</p>
				<p>
					 Учитывая отсутствие единообразия в терминологии законов можно предположить, что понятия «социальное обслуживание граждан» и «социальная услуга» являются составляющими понятий «социальная защита» и «социальная поддержка».
				</p>
				<p>
					 Существует еще один федеральный закон, который регулирует права инвалидов, это Федеральный закон N 178-ФЗ «О государственной социальной помощи».
				</p>
				<p>
					 Следует отметить, что в соответствии с Федеральным законом от 29.12.2015 N 388-ФЗ с 1 января 2018 года, в целях упорядочения системы понятий, используемых в отрасли социального обеспечения, предмет регулирования Федерального закона от 17 июля 1999 года N 178-ФЗ «О государственной социальной помощи» будет расширен. Абзац первый преамбулы этого закона будет сформулирован следующим образом:
				</p>
				<p>
					 "Настоящий Федеральный закон устанавливает правовые и организационные основы оказания государственной социальной помощи малоимущим семьям, малоимущим одиноко проживающим гражданам и иным категориям граждан, предусмотренным настоящим Федеральным законом, а также определяет порядок учета прав граждан на меры <b>социальной защиты (поддержки), социальные услуги, предоставляемые в рамках социального обслуживания и государственной социальной помощи, иные социальные гарантии и выплаты</b>, установленные законодательством Российской Федерации, законами и иными нормативными правовыми актами субъектов Российской Федерации, муниципальными нормативными правовыми актами".
				</p>
				<p>
					 Однако, такое расширение сферы действия закона не внесет полной ясности в структуру правоотношений в сфере социальной защиты инвалидов и межбюджетные взаимоотношения бюджетов разных уровней, поскольку внесенные изменения не содержит четкого соотношения используемых в законодательстве понятий:<b> «социальная защита», «социальная поддержка», «социальные услуги, предоставляемые в рамках социального обслуживания и государственной социальной помощи», «государственная социальная помощь», «социальная гарантия».</b>
				</p>
				<p>
					 Несмотря на то, что ФЗ № 442-ФЗ предоставил широкие полномочия субъектам федерации при осуществлении социальной поддержки, вопрос о порядке межбюджетного взаимодействия при финансировании гарантированной социальной защиты на законодательном до настоящего времени уровне не определен.
				</p>
				<p>
					 В соответствии со статьей 4 ФЗ № 181-ФЗ к ведению федеральных органов государственной власти в области социальной защиты инвалидов относятся:
				</p>
				<ol>
					<li>определение государственной политики в отношении инвалидов;</li>
					<li>принятие федеральных законов и иных нормативных правовых актов Российской Федерации о социальной защите инвалидов (в том числе регулирующих порядок и условия предоставления инвалидам единого федерального минимума мер социальной защиты); …</li>
					<li>установление общих принципов организации и осуществление медико-социальной экспертизы, реабилитации, абилитации инвалидов;</li>
					<li>определение критериев, установление условий для признания лица инвалидом;</li>
					<li>установление в соответствии с законодательством Российской Федерации о техническом регулировании обязательных требований к техническим средствам реабилитации, средствам связи и информатики, обеспечивающим доступность для инвалидов среды жизнедеятельности; …</li>
					<li>формирование показателей федерального бюджета по расходам на социальную защиту инвалидов»</li>
				</ol>
				<p>
					 Одновременно, в соответствии со статьей 5 ФЗ № 181-ФЗ участие органов государственной власти субъектов Российской Федерации в обеспечении социальной защиты и социальной поддержки инвалидов определяется следующим образом:
				</p>
				<p>
					 «Органы государственной власти субъектов Российской Федерации в области социальной защиты и социальной поддержки инвалидов имеют право:
				</p>
				<ol>
					<li>участия в реализации государственной политики в отношении инвалидов на территориях субъектов Российской Федерации;</li>
					<li>принятия в соответствии с федеральными законами законов и иных нормативных правовых актов субъектов Российской Федерации;</li>
					<li>участия в определении приоритетов в осуществлении социальной политики в отношении инвалидов на территориях субъектов Российской Федерации с учетом уровня социально-экономического развития этих территорий;</li>
					<li>разработки, утверждения и реализации региональных программ в области социальной защиты инвалидов в целях обеспечения им равных возможностей и социальной интеграции в общество…»</li>
				</ol>
				<p>
					 Таким образом, из пунктов 2) и 4) статьи 4 ФЗ № 181-ФЗ усматривается, что принятие федеральных законов и иных нормативных правовых актов Российской Федерации о социальной защите инвалидов (в том числе регулирующих порядок и условия предоставления инвалидам единого федерального минимума мер социальной защиты), а также установление общих принципов организации и осуществление медико-социальной экспертизы, реабилитации, и абилитации инвалидов отнесено к компетенции федеральных органов.
				</p>
				<p>
					 В то же время, из пунктов 3 и 4 статьи 5 ФЗ № 181-ФЗ следует, что социальная политика в отношении инвалидов, все же имеет местный приоритет и определяется с учетом уровня социально-экономического развития территорий. Данное положение закона позволяет предположить, что учет уровня социально-экономического развития субъекта федерации обусловлен при осуществлении социальной политики в отношении инвалидов обусловлен ограниченной возможностью бюджетного финансирования в субъектах федерации.
				</p>
				<p>
					 Между тем, в ст. 10 ФЗ № 181-ФЗ определено:
				</p>
				<p>
					 «Государство гарантирует инвалидам проведение реабилитационных мероприятий, получение технических средств и услуг, предусмотренных федеральным перечнем реабилитационных мероприятий, технических средств реабилитации и услуг, предоставляемых инвалиду за счет средств федерального бюджета.
				</p>
				<p>
					 Федеральный перечень реабилитационных мероприятий, технических средств реабилитации и услуг, предоставляемых инвалиду, утверждается Правительством Российской Федерации».
				</p>
				<p>
					 Таким образом, в законодательном подходе о порядке финансирования государственных гарантий в сфере социальной защиты и социального обеспечения инвалидов имеется пробел, который существенно тормозит процесс реализации прав инвалидов, гарантированных государством.
				</p>
				<p>
					 При отсутствии четкой и прозрачной системы взаимодействия между бюджетами разных уровней при реализации системы государственных гарантий, права инвалидов продолжают оставаться декларативными и не реализуют равенства возможностей инвалидов.
				</p>
				<p>
					 Следует отметить, что согласно ст. 31 ФЗ № 442-ФЗ социальные услуги в форме социального обслуживания на дому, в полустационарной и стационарной формах социального обслуживания несовершеннолетним детям предоставляются бесплатно.
				</p>
				<p>
					 Принимая во внимание, что наиболее высокий реабилитационный потенциал, в частности, у детей-инвалидов вследствие ортопедо-неврологических заболеваний сохраняется с раннего возраста и может быть реализован до наступления совершеннолетия, необходимо конкретизировать перечень социальных услуг, предусмотренных ст. 20 ФЗ № 442-ФЗ, дополнив услугами двигательной реабилитации, предоставляемыми детям-инвалидам вследствие ортопедо-неврологических заболеваний.
				</p>
				<p>
					 Услуги по двигательной реабилитации для детей-инвалидов вследствие ортопедо-неврологических заболеваний являются жизненно важными, позволяющими не только повысить их коммуникативный потенциал, но по достижении совершеннолетия интегрироваться в общество, проходить обучение и в дальнейшем самостоятельно трудиться.
				</p>
				<p>
					 При этом ранняя реабилитация таких детей-инвалидов позволит сократить нагрузку на бюджет за счет сокращения числа инвалидов с последствиями ДЦП, которые по достижении совершеннолетия не способны передвигаться, нуждаются в стационарном содержании и дорогостоящих социальных услугах домов-интернатов.
				</p>
				<p>
	 <b>Принимая во внимание, что статья 10 ФЗ № 181-ФЗ возлагает на федеральный бюджет финансирование гарантий инвалидам на проведение реабилитационных мероприятий, получение технических средств реабилитации и услуг, предусмотренных федеральным перечнем реабилитационных мероприятий, услуги по двигательной реабилитации детей-инвалидов вследствие ортопедо-неврологических заболеваний должны быть включены в федеральный перечень реабилитационных мероприятий как жизненно важные.</b>
				</p>
				<p>
					 Некоторое разграничение полномочий между Российской Федерацией и субъектами федерации содержится в статье 4.1. ФЗ № 178-ФЗ, определяющей полномочия Российской Федерации в области оказания государственной социальной помощи в виде набора социальных услуг, переданных для осуществления органам государственной власти субъектов Российской Федерации.
				</p>
				<p>
					 При этом согласно ч. 2 статьи 4.1. ФЗ № 178-ФЗ средства на осуществление переданных в соответствии с частью 1 настоящей статьи полномочий предусматриваются в виде субвенций из федерального бюджета.
				</p>
				<p>
					 Однако, услуги двигательной реабилитации для детей-инвалидов вследствие ортопедо-неврологических заболеваний в норму данной статьи не включены, что в свою очередь создает правовую неопределенность и не позволяет отнести их к гарантированной государственной социальной помощи.
				</p>
				<p>
					 Данный пробел в законодательстве также является существенной преградой в реализации субъектами федерации всех полномочий, предоставленных им ФЗ № 442-ФЗ при оказании социальной поддержки детям-инвалидам, нуждающимся в движении.
				</p>
				<p>
					 Существенным прорывом в законодательном регулировании стало внесение в ст. 9 ФЗ № 181-ФЗ понятий реабилитации (полного или частичного восстановления способностей инвалидов) и абилитации (формирования отсутствовавших у инвалидов способностей).
				</p>
				<p>
					 Однако из нормы ст. 9 ФЗ № 181-ФЗ невозможно установить, к какому виду социальной защиты и социальных услуг должны быть отнесены реабилитация и абилитация.
				</p>
				<p>
					 В соответствии с ч. 2 ст. 9 ФЗ № 181-ФЗ основные направления реабилитации и абилитации инвалидов включают в себя:
				</p>
				<ul>
